#!/usr/bin/env bun
/**
 * End-to-End Preprocessor Lambda Test
 *
 * Tests the full preprocessor pipeline:
 *   1. Creates a small synthetic test video locally (FFmpeg on your machine)
 *   2. Uploads it to the Remotion S3 bucket
 *   3. Invokes the preprocessor Lambda (deshake + sharpen + trim)
 *   4. Verifies the processed output exists in S3
 *   5. Downloads and checks the output
 *   6. Cleans up S3 artifacts
 *
 * Usage: bun scripts/test-preprocessor-e2e.ts
 *
 * Prerequisites:
 *   - REMOTION_AWS_ACCESS_KEY_ID and REMOTION_AWS_SECRET_ACCESS_KEY in .env
 *   - REMOTION_BUCKET_NAME in .env
 *   - PREPROCESSOR_FUNCTION_NAME in .env
 *   - FFmpeg installed locally (for test video generation)
 */

import { config } from 'dotenv';
config();

const region = process.env.REMOTION_AWS_REGION || 'us-east-1';
const bucketName = process.env.REMOTION_BUCKET_NAME;
const functionName = process.env.PREPROCESSOR_FUNCTION_NAME;

if (!bucketName) {
	console.error('ERROR: REMOTION_BUCKET_NAME not set in .env');
	process.exit(1);
}
if (!functionName) {
	console.error('ERROR: PREPROCESSOR_FUNCTION_NAME not set in .env');
	process.exit(1);
}
if (!process.env.REMOTION_AWS_ACCESS_KEY_ID || !process.env.REMOTION_AWS_SECRET_ACCESS_KEY) {
	console.error('ERROR: AWS credentials not set in .env');
	process.exit(1);
}

const testPrefix = `test-preprocessor-${Date.now()}`;
const inputS3Key = `temp-clips/${testPrefix}/input.mp4`;
const outputS3Key = `temp-clips/${testPrefix}/output.mp4`;

console.log('=== Preprocessor Lambda E2E Test ===');
console.log('Region:   %s', region);
console.log('Bucket:   %s', bucketName);
console.log('Function: %s', functionName);
console.log('Prefix:   %s', testPrefix);
console.log('');

async function createTestVideo(): Promise<Buffer> {
	const { execSync, spawnSync } = await import('child_process');
	const { readFileSync, unlinkSync, existsSync } = await import('fs');
	const { join } = await import('path');

	const tmpFile = join(process.cwd(), '.temp-test-video.mp4');

	// Check if local FFmpeg is available
	let ffmpegCmd = 'ffmpeg';
	try {
		execSync('ffmpeg -version', { stdio: 'pipe' });
	} catch {
		console.log('  FFmpeg not found on PATH, trying common locations...');
		// Try common Windows locations
		const locations = [
			'C:\\ffmpeg\\bin\\ffmpeg.exe',
			'C:\\Program Files\\ffmpeg\\bin\\ffmpeg.exe',
		];
		const found = locations.find(l => existsSync(l));
		if (found) {
			ffmpegCmd = `"${found}"`;
		} else {
			// Generate a minimal valid MP4 without FFmpeg using raw bytes
			console.log('  No local FFmpeg found. Generating minimal MP4 from bytes...');
			return createMinimalMp4();
		}
	}

	console.log('  Generating 3-second test video with %s...', ffmpegCmd);

	// Generate a simple color bar test video: 3 seconds, 640x360, 30fps
	try {
		execSync(
			`${ffmpegCmd} -y -f lavfi -i "color=c=blue:s=640x360:r=30:d=3" -f lavfi -i "sine=frequency=440:duration=3" -c:v libx264 -preset ultrafast -crf 28 -c:a aac -b:a 64k -shortest "${tmpFile}"`,
			{ stdio: 'pipe', timeout: 30000 },
		);
	} catch {
		// Try simpler command without audio
		execSync(
			`${ffmpegCmd} -y -f lavfi -i "color=c=blue:s=640x360:r=30:d=3" -c:v libx264 -preset ultrafast -crf 28 "${tmpFile}"`,
			{ stdio: 'pipe', timeout: 30000 },
		);
	}

	const buffer = readFileSync(tmpFile);
	try { unlinkSync(tmpFile); } catch { /* ok */ }

	return buffer;
}

