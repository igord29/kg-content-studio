async function uploadTestVideo(videoBuffer: Buffer): Promise<void> {
	const { S3Client, PutObjectCommand } = await import('@aws-sdk/client-s3');

	console.log('  Uploading %dKB test video to s3://%s/%s...',
		Math.round(videoBuffer.length / 1024), bucketName, inputS3Key);

	const s3 = new S3Client({
		region,
		credentials: {
			accessKeyId: process.env.REMOTION_AWS_ACCESS_KEY_ID!,
			secretAccessKey: process.env.REMOTION_AWS_SECRET_ACCESS_KEY!,
		},
	});

	await s3.send(new PutObjectCommand({
		Bucket: bucketName!,
		Key: inputS3Key,
		Body: videoBuffer,
		ContentType: 'video/mp4',
	}));

	console.log('  Uploaded.');
}

async function invokePreprocessor(): Promise<any> {
	const { LambdaClient, InvokeCommand } = await import('@aws-sdk/client-lambda');

	const payload = {
		bucketName,
		region,
		inputS3Key,
		outputS3Key,
		trimStart: 0,
		duration: 2,      // Process 2 seconds of the 3-second video
		speed: 1.0,
		stabilize: true,   // Enable deshake
		sharpen: true,     // Enable unsharp
	};

	console.log('  Invoking Lambda: %s', functionName);
	console.log('  Payload: trimStart=%d, duration=%d, stabilize=%s, sharpen=%s',
		payload.trimStart, payload.duration, payload.stabilize, payload.sharpen);

	const lambda = new LambdaClient({
		region,
		credentials: {
			accessKeyId: process.env.REMOTION_AWS_ACCESS_KEY_ID!,
			secretAccessKey: process.env.REMOTION_AWS_SECRET_ACCESS_KEY!,
		},
	});

	const startTime = Date.now();

	const result = await lambda.send(new InvokeCommand({
		FunctionName: functionName!,
		InvocationType: 'RequestResponse',
		Payload: Buffer.from(JSON.stringify(payload)),
	}));

	const elapsed = ((Date.now() - startTime) / 1000).toFixed(1);

	if (result.FunctionError) {
		const errorPayload = result.Payload
			? JSON.parse(Buffer.from(result.Payload).toString())
			: { errorMessage: 'Unknown error' };
		console.error('  Lambda RUNTIME ERROR after %ss:', elapsed);
		console.error('  %s', JSON.stringify(errorPayload, null, 2).substring(0, 1000));
		return null;
	}

	if (!result.Payload) {
		console.error('  Lambda returned no payload after %ss', elapsed);
		return null;
	}

	const response = JSON.parse(Buffer.from(result.Payload).toString());
	console.log('  Lambda response (took %ss):', elapsed);
	console.log('    success: %s', response.success);

	if (response.success) {
		console.log('    outputS3Key: %s', response.outputS3Key);
		console.log('    outputSize: %dKB', Math.round(response.outputSizeBytes / 1024));
		console.log('    effectiveDuration: %ss', response.effectiveDuration);
		console.log('    processingTime: %dms', response.processingTimeMs);
	} else {
		console.error('    error: %s', response.error);
	}

	return response;
}

async function verifyOutput(): Promise<boolean> {
	const { S3Client, HeadObjectCommand } = await import('@aws-sdk/client-s3');

	const s3 = new S3Client({
		region,
		credentials: {
			accessKeyId: process.env.REMOTION_AWS_ACCESS_KEY_ID!,
			secretAccessKey: process.env.REMOTION_AWS_SECRET_ACCESS_KEY!,
		},
	});

	try {
		const head = await s3.send(new HeadObjectCommand({
			Bucket: bucketName!,
			Key: outputS3Key,
		}));
		console.log('  Output file exists in S3: %dKB, ContentType: %s',
			Math.round((head.ContentLength || 0) / 1024), head.ContentType);
		return true;
	} catch (err: any) {
		if (err.name === 'NotFound' || err.$metadata?.httpStatusCode === 404) {
			console.error('  Output file NOT found in S3!');
			return false;
		}
		throw err;
	}
}

async function cleanup(): Promise<void> {
	const { S3Client, DeleteObjectCommand } = await import('@aws-sdk/client-s3');

	const s3 = new S3Client({
		region,
		credentials: {
			accessKeyId: process.env.REMOTION_AWS_ACCESS_KEY_ID!,
			secretAccessKey: process.env.REMOTION_AWS_SECRET_ACCESS_KEY!,
		},
	});

	console.log('  Cleaning up test artifacts...');

	for (const key of [inputS3Key, outputS3Key]) {
		try {
			await s3.send(new DeleteObjectCommand({
				Bucket: bucketName!,
				Key: key,
			}));
			console.log('    Deleted s3://%s/%s', bucketName, key);
		} catch {
			console.log('    Failed to delete %s (may not exist)', key);
		}
	}
}

async function main() {
	const startTime = Date.now();

	try {
		// Step 1: Create test video
		console.log('[1/5] Creating test video...');
		const videoBuffer = await createTestVideo();
		console.log('  Test video: %dKB', Math.round(videoBuffer.length / 1024));

		// Step 2: Upload to S3
		console.log('[2/5] Uploading to S3...');
		await uploadTestVideo(videoBuffer);

		// Step 3: Invoke preprocessor Lambda
		console.log('[3/5] Invoking preprocessor Lambda...');
		const response = await invokePreprocessor();

		if (!response || !response.success) {
			console.error('');
			console.error('══════════════════════════════════════════════');
			console.error('  TEST FAILED: Preprocessor Lambda returned error');
			console.error('══════════════════════════════════════════════');
			await cleanup();
			process.exit(1);
		}

		// Step 4: Verify output in S3
		console.log('[4/5] Verifying output in S3...');
		const outputExists = await verifyOutput();

		// Step 5: Cleanup
		console.log('[5/5] Cleaning up...');
		await cleanup();

		const totalTime = ((Date.now() - startTime) / 1000).toFixed(1);

		console.log('');
		if (outputExists) {
			console.log('══════════════════════════════════════════════');
			console.log('  TEST PASSED (%ss total)', totalTime);
			console.log('══════════════════════════════════════════════');
			console.log('');
			console.log('  FFmpeg preprocessor Lambda is working end-to-end:');
			console.log('  ✓ Test video uploaded to S3');
			console.log('  ✓ Lambda found FFmpeg binary');
			console.log('  ✓ FFmpeg deshake + sharpen + trim applied');
			console.log('  ✓ Processed output uploaded to S3');
			console.log('  ✓ Output verified and cleaned up');
			console.log('');
		} else {
			console.error('══════════════════════════════════════════════');
			console.error('  TEST FAILED: Output not found in S3 (%ss total)', totalTime);
			console.error('══════════════════════════════════════════════');
			process.exit(1);
		}

	} catch (err) {
		const msg = err instanceof Error ? err.message : String(err);
		console.error('');
		console.error('══════════════════════════════════════════════');
		console.error('  TEST FAILED: %s', msg);
		console.error('══════════════════════════════════════════════');
		await cleanup().catch(() => {});
		process.exit(1);
	}
}

main();
